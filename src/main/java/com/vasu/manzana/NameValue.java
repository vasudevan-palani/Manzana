package com.vasu.manzana;

public class NameValue{
	private String name;
	private String value;
	
	public void setValue(String value) {
		this.value = value;
	}
	
	public void setName(String name){
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public String getValue() {
		return value;
	}
	
}